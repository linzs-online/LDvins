void Estimator::changeSensorType(int use_imu, int use_stereo)
{
    bool restart = false;
    mProcess.lock();
    if(!use_imu && !use_stereo)
        printf("at least use two sensors! \n");
    else
    {
        if(USE_IMU != use_imu)
        {
            USE_IMU = use_imu;
            if(USE_IMU)
            {
                // reuse imu; restart system
                restart = true;
            }
            else
            {
                if (last_marginalization_info != nullptr)
                    delete last_marginalization_info;

                tmp_pre_integration = nullptr;
                last_marginalization_info = nullptr;
                last_marginalization_parameter_blocks.clear();
            }
        }
        
        STEREO = use_stereo;
        printf("use imu %d use stereo %d\n", USE_IMU, STEREO);
    }
    mProcess.unlock();
    if(restart)
    {
        clearState();
        setParameter();
    }
}

// 给Estimator输入图像
// 其实是给featureTracker.trackImage输入图像，之后返回图像特征featureFrame。填充featureBuf
// 之后执行processMeasurements
void Estimator::inputImage(double t, const cv::Mat &_img, const cv::Mat &_img1)
{   
    inputImageCnt++;
    map<int, vector<pair<int, Eigen::Matrix<double, 7, 1>>>> featureFrame;
    // 数据格式为feature_id camera_id（0或1） xyz_uv_velocity（空间坐标，像素坐标和像素速度）

    TicToc featureTrackerTime;
    // 特征追踪所用的时间 这个很好用啊！！

    if(_img1.empty())
        featureFrame = featureTracker.trackImage(t, _img);// 追踪单目
    else
        featureFrame = featureTracker.trackImage(t, _img, _img1);// 追踪双目
    printf("featureTracker time: %f ms \n", featureTrackerTime.toc());

    if (SHOW_TRACK)//这个应该是展示轨迹 
    {
        cv::Mat imgTrack = featureTracker.getTrackImage();
        pubTrackImage(imgTrack, t);
        
#ifdef SegmentDynamic
        cv::Mat imgSegment = featureTracker.getTrackSegment();
        pubSegmentImage(imgSegment, t);
#endif

    }
    
    if(MULTIPLE_THREAD) 
    {     
        if(inputImageCnt % 2 == 0)
        {
            mBuf.lock();
            featureBuf.push(make_pair(t, featureFrame));
            mBuf.unlock();
        }
    }
    else
    {
        mBuf.lock();
        featureBuf.push(make_pair(t, featureFrame));
        mBuf.unlock();
        TicToc processTime;
        processMeasurements(); //这里才执行了processMeasurements这个线程
        printf("process time: %f\n", processTime.toc());
    }
}

