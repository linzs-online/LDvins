// 对imu的时间进行判断，讲队列里的imu数据放入到accVector和gyrVector中，完成之后返回true
bool Estimator::getIMUInterval(double t0, double t1, vector<pair<double, Eigen::Vector3d>> &accVector, 
                                vector<pair<double, Eigen::Vector3d>> &gyrVector)
{
    if(accBuf.empty())
    {
        printf("not receive imu\n");
        return false;
    }
    //printf("get imu from %f %f\n", t0, t1);
    //printf("imu fornt time %f   imu end time %f\n", accBuf.front().first, accBuf.back().first);

    // 当现在时间小于队列里最后一个时间时
    if(accBuf.back().first >= t1)
    {
        //如果队列里第一个数据的时间小于起始时间，则删除第一个元素
        while (accBuf.front().first <= t0)
        {
            accBuf.pop();//.pop删除栈顶元素
            gyrBuf.pop();
        }
        // 讲队列里所有的acc和gyr输入到accvector个gyrvector中
        while (accBuf.front().first < t1)
        {
            accVector.push_back(accBuf.front());
            accBuf.pop();
            gyrVector.push_back(gyrBuf.front());
            gyrBuf.pop();
        }
        accVector.push_back(accBuf.front());
        gyrVector.push_back(gyrBuf.front());
    }
    else
    {
        printf("wait for imu\n");
        return false;
    }
    return true;
}

// 判断输入的时间t时候的imu是否可用
bool Estimator::IMUAvailable(double t)
{
    if(!accBuf.empty() && t <= accBuf.back().first)
        return true;
    else
        return false;
}

//处理量测的线程
void Estimator::processMeasurements()
{
    while (1)
    {
        //printf("process measurments\n");
        pair<double, map<int, vector<pair<int, Eigen::Matrix<double, 7, 1> > > > > feature;
        // 时间 特征点ID 图像id xyz_uv_vel
        vector<pair<double, Eigen::Vector3d>> accVector, gyrVector;
        //处理特征点的buf
        if(!featureBuf.empty())
        {            
            feature = featureBuf.front();//.front()返回当前vector容器中起始元素的引用。
            curTime = feature.first + td;//td的使用是在图像的时间上加上这个值
            while(1)
            {
                if ((!USE_IMU  || IMUAvailable(feature.first + td)))//如果不用imu或者  IMU数据时间不匹配
                    break;
                else
                {
                    //printf("wait for imu ... \n");
                    if (! MULTIPLE_THREAD)
                        return;
                    std::chrono::milliseconds dura(5);//定义5ms的延迟
                    std::this_thread::sleep_for(dura);//这个线程延迟5ms
                }
            }
            mBuf.lock();
            if(USE_IMU)
                // 对imu的时间进行判断，讲队列里的imu数据放入到accVector和gyrVector中，完成之后返回true
                getIMUInterval(prevTime, curTime, accVector, gyrVector);

            featureBuf.pop();//每次运行完之后都删除featureBuf中的元素，直到为空，已经把要删除的这个值给了feature
            mBuf.unlock();

            // 处理imu数据，运行processIMU
            if(USE_IMU)
            {
                if(!initFirstPoseFlag)
                    initFirstIMUPose(accVector);
                for(size_t i = 0; i < accVector.size(); i++)
                {
                    double dt;//计算每次imu量测之间的dt
                    if(i == 0)
                        dt = accVector[i].first - prevTime;
                    else if (i == accVector.size() - 1)
                        dt = curTime - accVector[i - 1].first;
                    else
                        dt = accVector[i].first - accVector[i - 1].first;

                    processIMU(accVector[i].first, dt, accVector[i].second, gyrVector[i].second);  // 刚开始的第一帧的时候实际上虽然 prevTime = -1，但是 dt根本没用上，所以不会造成歧义
                }
            }
            mProcess.lock();
            processImage(feature.second, feature.first);
            prevTime = curTime;

            printStatistics(*this, 0);

            std_msgs::Header header;
            header.frame_id = "world";
            header.stamp = ros::Time(feature.first);

            pubOdometry(*this, header);
            pubKeyPoses(*this, header);
            pubCameraPose(*this, header);
            pubPointCloud(*this, header);
            pubKeyframe(*this);
            pubTF(*this, header);
            mProcess.unlock();
        }

        if (! MULTIPLE_THREAD)
            break;

        std::chrono::milliseconds dura(2);
        std::this_thread::sleep_for(dura);
    }
}

//初始第一个imu位姿
void Estimator::initFirstIMUPose(vector<pair<double, Eigen::Vector3d>> &accVector)
{
    printf("init first imu pose\n");
    initFirstPoseFlag = true;
    //return;
    //计算加速度的均值
    Eigen::Vector3d averAcc(0, 0, 0);
    int n = (int)accVector.size();
    for(size_t i = 0; i < accVector.size(); i++)
    {
        averAcc = averAcc + accVector[i].second;
    }
    averAcc = averAcc / n;
    printf("averge acc %f %f %f\n", averAcc.x(), averAcc.y(), averAcc.z());

    Matrix3d R0 = Utility::g2R(averAcc);
    double yaw = Utility::R2ypr(R0).x();
    R0 = Utility::ypr2R(Eigen::Vector3d{-yaw, 0, 0}) * R0;//另初始的航向为0
    Rs[0] = R0;
    cout << "init R0 " << endl << Rs[0] << endl;
    //Vs[0] = Vector3d(5, 0, 0);
}

